      sha256 "a5e8173cd7ff9783182ba3462be83348bb8f6a2a145728d95a892660196d35f3"
    end
  end

  on_linux do
    if Hardware::CPU.intel?
      url "https://github.com/myiam-io/homebrew-tap/releases/download/due2-cli-v#{version}/due2-cli-#{version}-linux-x64.tar.gz"
      sha256 "9ce63183ea52bcf4cd866d4a859b385c1910631f5ec1a02b08967d02b612a365"
    end
  end

  def install
    bin.install "due2-cli"
  end

  test do
    assert_match "due2-cli", shell_output("#{bin}/due2-cli --help")
  end
end
